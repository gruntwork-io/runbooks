import { useIpc } from './useIpc';
import type { UseIpcReturn } from './useIpc';
import { useMemo, useCallback, useEffect, startTransition } from 'react';
import { useFileTreeUpdater } from '@/components/mdx/_shared/hooks/useFileTreeUpdater';
import type { FileTreeNode } from '@/components/artifacts/code/FileTree';
import type { HeavyDir } from '@/contexts/GeneratedFilesContext.types';

interface BoilerplateRenderResult {
  message:      string,
  outputDir:    string,
  templatePath: string,
  fileTree:     FileTreeNode[],
  totalFiles?:  number,
  truncatedTree?: boolean,
  heavyDirs?: HeavyDir[],
  deletedFiles?:  string[],
  createdFiles?:  string[],
  modifiedFiles?: string[],
  skippedFiles?:  string[],
}

// Enhanced return type that includes auto-rendering functionality
interface UseApiBoilerplateRenderResult extends UseIpcReturn<BoilerplateRenderResult> {
  isAutoRendering: boolean;
  autoRender: (templatePath: string, variables: Record<string, unknown>) => void;
}

/**
 * Hook for rendering boilerplate templates via the API.
 * 
 * @param templatePath - Path to the boilerplate template directory (required)
 * @param templateId - Unique ID of the Template component (required)
 * @param variables - Variables to pass to the template
 * @param shouldFetch - Whether to fetch immediately (default: true)
 * @param target - Where template output is written: "generated" (default) or "worktree"
 * 
 * The backend uses templateId to track which files were generated by this template.
 * On re-render, it automatically cleans up orphaned files (e.g., when switching
 * from Python to Node.js runtime) and only writes files that have changed.
 */
export function useApiBoilerplateRender(
  templatePath: string,
  templateId: string,
  variables?: Record<string, unknown>,
  shouldFetch: boolean = true,
  target?: 'generated' | 'worktree'
): UseApiBoilerplateRenderResult {
  const { applyFileTreeUpdate } = useFileTreeUpdater(target);

  // Build the request body - both templatePath and templateId are required
  const requestBody = useMemo(() => {
    if (!shouldFetch) return null;
    if (templatePath && templateId) {
      return { templatePath, templateId, variables, ...(target ? { target } : {}) };
    }
    return null;
  }, [templatePath, templateId, variables, shouldFetch, target]);

  // Always lazy: we never want useApi's body-change auto-fetch to dispatch
  // a render. Render is driven exclusively through `autoRender` (the
  // debounced path). Before lazy mode, flipping `shouldFetch` true on
  // Generate fired one render immediately AND a second via debouncedRequest
  // from Template's auto-render effect — doubling the work on every click.
  // No debounce here: useFormState already debounces upstream, and the main
  // process supersedes in-flight renders via fiber-interrupt (see
  // electron/main/ipc/boilerplate.ts activeRenders), so back-to-back invokes
  // never queue up. Adding a second debounce just stacked extra latency.
  const apiResult = useIpc<BoilerplateRenderResult>(
    shouldFetch ? 'boilerplate:render' : '',
    requestBody || undefined,
    { lazy: true, debounceMs: 0 },
  );

  const { debouncedRequest } = apiResult;
  const autoRender = useCallback((templatePath: string, variables: Record<string, unknown>) => {
    if (debouncedRequest && templatePath && templateId) {
      debouncedRequest({ templatePath, templateId, variables, ...(target ? { target } : {}) });
    }
  }, [debouncedRequest, templateId, target]);

  // Handle file tree updates when data changes.
  //
  // `applyFileTreeUpdate` bumps `treeVersion` in GitWorkTreeContext (and,
  // for non-worktree targets, also sets generated-files state). That
  // synchronously re-renders every consumer of those contexts — Workspace,
  // RepositoryFileBrowser, the changes panel, etc. — in the same React
  // commit as our render result. Measured paint cost: ~97ms after data
  // arrives, almost entirely due to this cascade.
  //
  // Wrapping in startTransition tells React the workspace refresh is
  // lower-priority than painting the template-render result. The Template
  // preview lands on next frame, the workspace catches up on the frame
  // after (or two) without blocking the visible result.
  const renderData = apiResult.data;
  useEffect(() => {
    if (renderData?.fileTree && Array.isArray(renderData.fileTree)) {
      startTransition(() => {
        applyFileTreeUpdate(renderData);
      });
    }
  }, [renderData, applyFileTreeUpdate]);

  return {
    ...apiResult,
    isAutoRendering: apiResult.isLoading,
    autoRender
  };
}